      const g = groupBy(rows, r => `${r.machineName}|${r.provenanceDepth}`);
      const out = [...g.entries()].map(([k, list]) => {
        const [machine, depth] = k.split('|');
        return { machine, chainDepth: Number(depth), fires: list.length };
      }).sort((a, b) => a.machine.localeCompare(b.machine) || a.chainDepth - b.chainDepth);
      return out.slice(0, 40);
