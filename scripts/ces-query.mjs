// ── Output formatting ───────────────────────────────────────────────────────

function tabulate(rows) {
  if (!Array.isArray(rows)) return JSON.stringify(rows, null, 2);
  if (rows.length === 0) return '(no rows)';
  const cols = Object.keys(rows[0]);
  const widths = cols.map(c => Math.max(c.length, ...rows.map(r => String(r[c] ?? '').length)));
  const sep = widths.map(w => '─'.repeat(w)).join('─┼─');
  const head = cols.map((c, i) => c.padEnd(widths[i])).join(' │ ');
  const body = rows.map(r => cols.map((c, i) => String(r[c] ?? '').padEnd(widths[i])).join(' │ ')).join('\n');
  return `${head}\n${sep}\n${body}`;
}

function describe(q) {
  return `${q.id.padEnd(4)} ${q.label}\n     ❯ ${q.operationalQuestion}\n     DSL: ${q.dsl}`;
}

// ── CLI ─────────────────────────────────────────────────────────────────────

function parseArgs(argv) {
  const args = { list: false, all: false, query: null, json: false, trace: null, machinesDir: null };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--list') args.list = true;
    else if (a === '--all') args.all = true;
    else if (a === '--query') args.query = argv[++i];
    else if (a === '--json') args.json = true;
    else if (a === '--trace') args.trace = path.resolve(argv[++i]);
    else if (a === '--machines-dir') args.machinesDir = path.resolve(argv[++i]);
    else if (a === '-h' || a === '--help') { printHelp(); process.exit(0); }
    else { console.error(`unknown argument: ${a}`); process.exit(2); }
  }
  args.trace       ??= path.join(ROOT, 'examples', 'contracts.json');
  args.machinesDir ??= path.join(ROOT, 'examples', 'machines');
  return args;
}

function printHelp() {
  console.log('Usage: ces-query [--list | --all | --query Q1] [--json] [--trace PATH] [--machines-dir DIR]');
  console.log('');
  console.log('Canned queries:');
  for (const q of QUERIES) console.log('  ' + describe(q));
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  if (args.list) { printHelp(); return; }

  const trace = flattenContracts(args.trace, args.machinesDir);
  console.log(`# trace rows: ${trace.rows.length}  (from ${path.relative(ROOT, args.trace)})\n`);

  const run = (q) => {
    const result = q.run(trace.rows, trace);
    if (args.json) { console.log(JSON.stringify({ id: q.id, label: q.label, dsl: q.dsl, result }, null, 2)); return; }
    console.log(`══ ${q.id}: ${q.label} ══════════════════════════════════════════════`);
    console.log(`❯ ${q.operationalQuestion}`);
    console.log(`DSL: ${q.dsl}`);
    console.log('');
    console.log(tabulate(result));
    console.log('');
  };

  if (args.all) { for (const q of QUERIES) run(q); return; }
  if (args.query) {
    const q = QUERIES.find(x => x.id === args.query);
    if (!q) { console.error(`unknown query: ${args.query}`); process.exit(2); }
    run(q);
    return;
  }
  printHelp();
}

main();
