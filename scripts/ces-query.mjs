#!/usr/bin/env node
/**
 * ces-query — observability DSL over CES activation traces.
 *
 * Step 11 in the CES-roadmap: every vector has a stable id, every
 * mergeBatch entry carries (machine, sequence, provenance, governance,
 * deprecation, region, values).  That structured payload is a query
 * surface — a small DSL lets operators ask questions like
 *
 *   "Where machine='Fall Detection' AND lastVector='fall-conf-v4'
 *    GROUP BY hourOfDay"
 *
 * without writing custom logging code in the runtime.  This CLI runs
 * such queries against examples/contracts.json — the canonical replay of
 * 5,589 simulation steps generated by scripts/cesgen-contracts.mjs.  In
 * production the same DSL would back a panel against streamed engine
 * traces.
 *
 * Usage:
 *   node scripts/ces-query.mjs --list                 # show all canned queries
 *   node scripts/ces-query.mjs --query Q1             # run by id
 *   node scripts/ces-query.mjs --query Q1 --json      # JSON output
 *   node scripts/ces-query.mjs --all                  # run every query
 *   node scripts/ces-query.mjs --trace PATH           # alternate trace file
 */

import fs from 'node:fs';
import path from 'node:path';

const ROOT = path.resolve(path.dirname(new URL(import.meta.url).pathname), '..');

// ── Synthetic time stamping ──────────────────────────────────────────────────

/**
 * The contracts file captures structural outputs but not when in wall-clock
 * time they happened.  For the demo we fabricate per-event timestamps by
 * hashing (contractId + stepIndex) into a 24-hour band.  Deterministic,
 * spread across the day, just enough for hour-of-day groupings to look
 * realistic.  A production deployment would use the engine's own
 * timestamps from /api/perceive or a streaming pipeline.
 */
function fnv1a(s) {
  let h = 0x811c9dc5;
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = (h * 0x01000193) >>> 0;
  }
  return h;
}
function syntheticHourAndMinute(contractId, stepIdx, occurrence) {
  const h = fnv1a(`${contractId}:${stepIdx}:${occurrence}`);
  return {
    hourOfDay: h % 24,
    minute:    Math.floor(h / 24) % 60,
  };
}
/**
 * Per-contract replay count.  The contracts file pins ONE canonical
 * occurrence per chain, but a real activity log carries each chain
 * multiple times across a day.  We amplify deterministically so the
 * hour-of-day rollups show meaningful distributions without bloating
 * the trace file.  Range: 5..30 occurrences per chain.
 */
function replayCount(contractId) {
  return (fnv1a(`replay:${contractId}`) % 26) + 5;
}

// ── Trace flattener ──────────────────────────────────────────────────────────

/**
 * Flatten the contract file into one row per mergeBatch entry — the
 * shape the DSL queries operate on.  Joins the contract id (so callers
 * can group by chain), the synthetic timestamp, and the engine fields.
 *
 * Note: governance / deprecation are absent because cesgen-contracts
 * deliberately projects them out (see scripts/cesgen-contracts.mjs);
 * production traces would carry the full block.  We backfill governance
 * synthetically by looking up the machine JSON's triggerConfig.rules
 * so the governance-flavored queries can demonstrate value.
 */
function flattenContracts(file, machinesDir) {
  const data = JSON.parse(fs.readFileSync(file, 'utf8'));

  // Build a per-machine sequence→rule lookup so we can backfill (rag,
  // status, ownerTeam) onto each row without re-running the engine.
  const ruleByMachine = new Map();
  for (const c of data.contracts) {
    if (ruleByMachine.has(c.machineFile)) continue;
    try {
      const raw = JSON.parse(fs.readFileSync(path.join(machinesDir, c.machineFile), 'utf8'));
      const m = raw.machine ?? raw;
      const rules = m.metadata?.triggerConfig?.rules ?? [];
      const gov = m.metadata?.governance ?? null;
      ruleByMachine.set(c.machineFile, { machine: m, rules, governance: gov });
    } catch (e) { ruleByMachine.set(c.machineFile, { machine: null, rules: [], governance: null }); }
  }

  const rows = [];
  for (const c of data.contracts) {
    const meta = ruleByMachine.get(c.machineFile);
    const machineName = meta?.machine?.name ?? c.machineFile;
    const replays = replayCount(c.id);
    for (let occ = 0; occ < replays; occ++) {
      for (const step of c.outputStream) {
        const t = syntheticHourAndMinute(c.id, step.step, occ);
        for (const op of step.mergeBatch) {
          const rule = (meta?.rules ?? []).find(r =>
            r.sequenceId === op.sequenceId &&
            arraysEqual(r.outputMatches, op.values));
          const ragStatusCode = rule?.ragStatusCode ?? null;
          const processStatus = rule?.processStatus ?? null;
          const ownerTeam = (rule?.governance?.ownerTeam) ?? meta?.governance?.ownerTeam ?? 'unrouted';
          rows.push({
            contractId:      c.id,
            occurrence:      occ,
            machineFile:     c.machineFile,
            machineName,
            step:             step.step,
            sequenceId:       op.sequenceId,
            lastVector:       op.provenance?.length ? op.provenance[op.provenance.length - 1] : null,
            provenance:       op.provenance ?? [],
            provenanceDepth: (op.provenance ?? []).length,
            regionOffset:     op.region.offset,
            regionLength:     op.region.length,
            values:           op.values,
            ragStatusCode,
            processStatus,
            ownerTeam,
            hourOfDay:        t.hourOfDay,
            minute:           t.minute,
            timeOfDay:        `${String(t.hourOfDay).padStart(2,'0')}:${String(t.minute).padStart(2,'0')}`,
          });
        }
      }
    }
  }
  return { rows, ruleByMachine };
}

function arraysEqual(a, b) {
  if (!Array.isArray(a) || !Array.isArray(b) || a.length !== b.length) return false;
  for (let i = 0; i < a.length; i++) if (a[i] !== b[i]) return false;
  return true;
}

// ── Tiny query helpers ──────────────────────────────────────────────────────

function groupBy(rows, keyFn) {
  const out = new Map();
  for (const r of rows) {
    const k = keyFn(r);
    const list = out.get(k);
    if (list) list.push(r); else out.set(k, [r]);
  }
  return out;
}

function topK(map, k, valueFn = v => v.length) {
  return [...map.entries()]
    .map(([key, value]) => ({ key, value, n: valueFn(value) }))
    .sort((a, b) => b.n - a.n)
    .slice(0, k);
}

